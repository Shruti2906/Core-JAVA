
public class Demo {

	public void greater(int a, int b) {
		if(a>b)
			System.out.println("Greater is : "+a);
		else 
			System.out.println("Greater is : "+b);
	}
	
	public void greater(float a, float b) {
		if(a>b)
			System.out.println("Greater is : "+a);
		else 
			System.out.println("Greater is : "+b);
	}
	
	public void greater(char a, char b) {
		if(a>b)
			System.out.println("Greater is : "+a);
		else 
			System.out.println("Greater is : "+b);
	}
	
}
