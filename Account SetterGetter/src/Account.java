
public class Account {

	private int accNo;
	private String AccName;
	private double Bal;
	
	public int getAccNo() {
		return accNo;
	}
	public void setAccNo(int accNo) {
		this.accNo = accNo;
	}
	public String getAccName() {
		return AccName;
	}
	public void setAccName(String accName) {
		AccName = accName;
	}
	public double getBal() {
		return Bal;
	}
	public void setBal(double bal) {
		Bal = bal;
	}
	
}
